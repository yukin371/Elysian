describe("planModuleFiles", () => {
  it("plans vue page output by default", () => {
    expect(planModuleFiles(customerModuleSchema)).toEqual([
      {
        path: "modules/customer/customer.schema.ts",
        reason:
          "Persist the module schema alongside generated module artifacts.",
        mergeStrategy: "replace-whole-file",
      },
      {
        path: "modules/customer/customer.persistence.ts",
        reason:
          "Emit a Drizzle persistence schema template for manual integration into packages/persistence.",
        mergeStrategy: "replace-whole-file",
      },
      {
        path: "modules/customer/customer.repository.ts",
        reason: "Provide the persistence-facing module repository boundary.",
        mergeStrategy: "replace-whole-file",
      },
      {
        path: "modules/customer/customer.service.ts",
        reason: "Host the generated application service entry for the module.",
        mergeStrategy: "replace-whole-file",
      },
      {
        path: "modules/customer/customer.routes.ts",
        reason:
          "Expose the server-side route registration entry for the module.",
        mergeStrategy: "replace-whole-file",
      },
      {
        path: "modules/customer/customer.frontend.ts",
        reason:
          "Emit a static frontend registration artifact for generated module integration.",
        mergeStrategy: "replace-whole-file",
      },
      {
        path: "modules/customer/customer.page.vue",
        reason:
          "Provide a generated management page implementation for the selected frontend target.",
        mergeStrategy: "replace-whole-file",
      },
    ])
  })

  it("uses module-relative paths and includes module registration when target is module", () => {
    const plans = planModuleFiles(customerModuleSchema, {
      targetPreset: "module",
    })

    expect(plans.map((plan) => plan.path)).toEqual([
      "customer/customer.schema.ts",
      "customer/customer.persistence.ts",
      "customer/customer.repository.ts",
      "customer/customer.service.ts",
      "customer/customer.routes.ts",
      "customer/customer.frontend.ts",
      "customer/customer.module.ts",
      "customer/customer.page.vue",
    ])
    expect(plans[6]).toEqual({
      path: "customer/customer.module.ts",
      reason:
        "Emit a server module assembly stub that bridges repository wiring into generated routes.",
      mergeStrategy: "preserve-existing",
    })
  })

  it("includes persistence schema in file plan", () => {
    const schema: ModuleSchema = {
      name: "supplier",
      label: "Supplier",
      fields: [
        { key: "id", label: "ID", kind: "id", required: true },
        { key: "name", label: "Name", kind: "string", required: true },
      ],
    }

    const plans = planModuleFiles(schema)
    const persistenceFile = plans.find((plan) =>
      plan.path.endsWith(".persistence.ts"),
    )

    expect(persistenceFile).toBeDefined()
    expect(persistenceFile?.path).toContain("supplier")
  })
})

